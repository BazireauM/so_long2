#include "ft_printf.h"

int	choose_c(va_list arg, int *a)
{
	char	d;
	int		b;

	d = va_arg(arg, int);
	b = write(1, &d, 1);
	if (b == -1)
		return (-1);
	*a += b;
	return (0);
}
