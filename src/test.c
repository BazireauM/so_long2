#include "../include/so_long.h"

int	test_name_file(char *s)
{
	int	i;
	int	b;

	i = 0;
	b = 0;
	while (s[i])
	{
		if (s[i] == '.')
			b++;
		i++;
	}
	if (b != 1)
		return (1);
	if (s[i - 1] == 'r' && s[i - 2] == 'e' && s[i - 3] == 'b'
		&& s[i - 4] == '.')
		return (0);
	return (1);
}
